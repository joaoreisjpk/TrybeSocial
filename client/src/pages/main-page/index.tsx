import { GetServerSideProps } from 'next';
import Head from 'next/head';
import React, { useEffect } from 'react';
import Header from '../../components/Header';
import { fetchRefreshToken } from '../../helpers/fetchers';
import JWT, { decrypt, encrypt } from '../../helpers/Encrypt';
import { useAuth } from '../../hooks/useAuth';
import { CookieAt, CookieRt } from '../../helpers/cookie';

interface ITokens {
  tokens: {
    tokenAt: string;
    tokenRt: string;
  };
}

export default function MainPage({ tokens }: ITokens) {
  const { email } = useAuth();

  useEffect(() => {
    CookieAt.set('tokenAt', encrypt(tokens.tokenAt));
    CookieRt.set('tokenRt', encrypt(tokens.tokenRt));
  }, []);

  return (
    <div>
      <Head>
        <title>Main Page - Trybe Social</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <Header />
      <h1>Main Page</h1>
      <h2>Você está logado com o email: {email}</h2>
    </div>
  );
}

export const getServerSideProps: GetServerSideProps = async ({ req }) => {
  const { tokenAt: AtCrypted, tokenRt: RtCrypted } = req.cookies;

  const tokenAt = decrypt(AtCrypted);
  const tokenRt = decrypt(RtCrypted);

  console.log('realod main-page');

  if (!tokenRt) {
    return {
      props: {},
      redirect: {
        destination: '/login',
        permanent: false,
      },
    };
  }

  const jwt = new JWT();
  const { email: userEmail } = jwt.decode(tokenAt);
  const { acess_token, refresh_token, error } = await fetchRefreshToken(
    tokenRt,
    userEmail
  );

  console.log(error);

  if (error) {
    return {
      props: {},
      redirect: {
        destination: '/login',
        permanent: false,
      },
    };
  }

  return {
    props: {
      tokens: {
        tokenAt: acess_token,
        tokenRt: refresh_token,
      },
    },
  };
};
