import { GetServerSideProps } from 'next';
import Head from 'next/head';
import React from 'react';
import Header from '../../components/Header';
import { fetchRefreshToken } from '../../helpers/fetchers';
import JWT, { decrypt, encrypt } from '../../helpers/Encrypt';
import { useAuth } from '../../hooks/useAuth';

export default function MainPage() {
  const { email } = useAuth();
  return (
    <div>
      <Head>
        <title>Main Page - Trybe Social</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <Header />
      <h1>Main Page</h1>
      <h2>Você está logado com o email: {email}</h2>
    </div>
  );
}

export const getServerSideProps: GetServerSideProps = async ({ req }) => {
  const { tokenAt: AtCrypted, tokenRt: RtCrypted } = req.cookies;

  const tokenAt = decrypt(AtCrypted);
  const tokenRt = decrypt(RtCrypted);

  console.log(tokenAt);

  if (!tokenRt) {
    return {
      props: {},
      redirect: {
        destination: '/login',
        permanent: false,
      },
    };
  }

  const jwt = new JWT();
  const { email: userEmail } = jwt.decode(tokenAt);
  const { acess_token, refresh_token, error } = await fetchRefreshToken(
    tokenRt,
    userEmail
  );

  if (error) {
    return {
      props: {},
      redirect: {
        destination: '/login',
        permanent: false,
      },
    };
  }

  req.cookies.tokenRt = refresh_token as string;
  req.cookies.tokenAt = acess_token as string;

  return {
    props: {},
  };
};
