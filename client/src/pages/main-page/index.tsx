import { GetServerSideProps } from 'next';
import Head from 'next/head';
import React from 'react';
import Header from '../../components/Header';
import { fetchRefreshToken } from '../../helpers/fetchers';
import { useAuth } from '../../hooks/useAuth';

export default function MainPage() {
  const { email } = useAuth();
  return (
    <div>
      <Head>
        <title>Main Page - Trybe Social</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <Header />
      <h1>Main Page</h1>
      <h2>Você está logado com o email: {email}</h2>
    </div>
  );
}

export const getServerSideProps: GetServerSideProps = async ({ req }) => {
  const { tokenAt, tokenRt, userEmail } = req.cookies;

  if (!tokenRt || !userEmail) {
    return {
      props: {},
      redirect: {
        destination: '/login',
      },
    };
  }

  const tokens = await fetchRefreshToken(tokenRt, userEmail);

  if (tokens.refresh_token)
    return {
      props: {},
      redirect: {
        destination: '/login',
      },
    };

  return {
    props: {},
  };
};
